package Number;

public class ComplexNumber {
    private double re;
    private double im;

    public ComplexNumber() {
    }

    public ComplexNumber(double re, double im) {
        this.re = re;
        this.im = im;

    }

    public double getRe() {
        return re;
    }

    public double getIm() {
        return im;
    }


}